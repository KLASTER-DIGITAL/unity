#!/bin/bash

# –°–∫—Ä–∏–ø—Ç –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏ –±–∏—Ç—ã—Ö —Å—Å—ã–ª–æ–∫ –≤ –¥–æ–∫—É–º–µ–Ω—Ç–∞—Ü–∏–∏
# –ê–≤—Ç–æ—Ä: Product Team UNITY
# –î–∞—Ç–∞: 21 –æ–∫—Ç—è–±—Ä—è 2025

set -e

echo "üîç –ü—Ä–æ–≤–µ—Ä–∫–∞ –±–∏—Ç—ã—Ö —Å—Å—ã–ª–æ–∫ –≤ –¥–æ–∫—É–º–µ–Ω—Ç–∞—Ü–∏–∏..."
echo ""

# –°—á–µ—Ç—á–∏–∫–∏
TOTAL_LINKS=0
BROKEN_LINKS=0
EXTERNAL_LINKS=0

# –í—Ä–µ–º–µ–Ω–Ω—ã–π —Ñ–∞–π–ª –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–≤
TEMP_FILE=$(mktemp)

# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏ —Å—É—â–µ—Å—Ç–≤–æ–≤–∞–Ω–∏—è —Ñ–∞–π–ª–∞
check_link() {
    local file="$1"
    local link="$2"
    local dir=$(dirname "$file")
    
    # –ü—Ä–æ–ø—É—Å—Ç–∏—Ç—å —è–∫–æ—Ä—è (anchors)
    if [[ $link == \#* ]]; then
        return 0
    fi
    
    # –ü—Ä–æ–ø—É—Å—Ç–∏—Ç—å –≤–Ω–µ—à–Ω–∏–µ —Å—Å—ã–ª–∫–∏ (http/https)
    if [[ $link == http* ]]; then
        ((EXTERNAL_LINKS++))
        return 0
    fi
    
    ((TOTAL_LINKS++))
    
    # –£–±—Ä–∞—Ç—å —è–∫–æ—Ä—å –∏–∑ —Å—Å—ã–ª–∫–∏, –µ—Å–ª–∏ –µ—Å—Ç—å
    link_without_anchor="${link%%\#*}"
    
    # –û–ø—Ä–µ–¥–µ–ª–∏—Ç—å –∞–±—Å–æ–ª—é—Ç–Ω—ã–π –ø—É—Ç—å –∫ —Ü–µ–ª–µ–≤–æ–º—É —Ñ–∞–π–ª—É
    if [[ $link_without_anchor == /* ]]; then
        # –ê–±—Å–æ–ª—é—Ç–Ω–∞—è —Å—Å—ã–ª–∫–∞ –æ—Ç –∫–æ—Ä–Ω—è —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏—è
        target="$link_without_anchor"
    else
        # –û—Ç–Ω–æ—Å–∏—Ç–µ–ª—å–Ω–∞—è —Å—Å—ã–ª–∫–∞
        target="$dir/$link_without_anchor"
    fi
    
    # –ù–æ—Ä–º–∞–ª–∏–∑–æ–≤–∞—Ç—å –ø—É—Ç—å
    target=$(cd "$(dirname "$target")" 2>/dev/null && pwd)/$(basename "$target") 2>/dev/null || echo "$target"
    
    # –ü—Ä–æ–≤–µ—Ä–∏—Ç—å —Å—É—â–µ—Å—Ç–≤–æ–≤–∞–Ω–∏–µ —Ñ–∞–π–ª–∞
    if [ ! -f "$target" ] && [ ! -d "$target" ]; then
        echo "‚ùå –ë–∏—Ç–∞—è —Å—Å—ã–ª–∫–∞ –≤ $file:" >> "$TEMP_FILE"
        echo "   –°—Å—ã–ª–∫–∞: $link" >> "$TEMP_FILE"
        echo "   –¶–µ–ª–µ–≤–æ–π —Ñ–∞–π–ª –Ω–µ –Ω–∞–π–¥–µ–Ω: $target" >> "$TEMP_FILE"
        echo "" >> "$TEMP_FILE"
        ((BROKEN_LINKS++))
        return 1
    fi
    
    return 0
}

# –ù–∞–π—Ç–∏ –≤—Å–µ .md —Ñ–∞–π–ª—ã –≤ docs/
echo "üìÇ –°–∫–∞–Ω–∏—Ä–æ–≤–∞–Ω–∏–µ —Ñ–∞–π–ª–æ–≤ –≤ docs/..."
MD_FILES=$(find docs -name "*.md" -type f | sort)
FILE_COUNT=$(echo "$MD_FILES" | wc -l | tr -d ' ')
echo "   –ù–∞–π–¥–µ–Ω–æ —Ñ–∞–π–ª–æ–≤: $FILE_COUNT"
echo ""

# –ü—Ä–æ–≤–µ—Ä–∏—Ç—å –∫–∞–∂–¥—ã–π —Ñ–∞–π–ª
CURRENT=0
for file in $MD_FILES; do
    ((CURRENT++))
    
    # –ü—Ä–æ–≥—Ä–µ—Å—Å
    if [ $((CURRENT % 10)) -eq 0 ]; then
        echo "   –ü—Ä–æ–≤–µ—Ä–µ–Ω–æ: $CURRENT / $FILE_COUNT —Ñ–∞–π–ª–æ–≤..."
    fi
    
    # –ò–∑–≤–ª–µ—á—å –≤—Å–µ markdown —Å—Å—ã–ª–∫–∏ –≤–∏–¥–∞ [text](link)
    # –ò—Å–ø–æ–ª—å–∑—É–µ–º sed –¥–ª—è —Å–æ–≤–º–µ—Å—Ç–∏–º–æ—Å—Ç–∏ —Å macOS
    links=$(sed -n 's/.*\](\([^)]*\)).*/\1/p' "$file" 2>/dev/null || true)
    
    # –ü—Ä–æ–≤–µ—Ä–∏—Ç—å –∫–∞–∂–¥—É—é —Å—Å—ã–ª–∫—É
    while IFS= read -r link; do
        if [ -n "$link" ]; then
            check_link "$file" "$link" || true
        fi
    done <<< "$links"
done

echo ""
echo "‚úÖ –°–∫–∞–Ω–∏—Ä–æ–≤–∞–Ω–∏–µ –∑–∞–≤–µ—Ä—à–µ–Ω–æ"
echo ""

# –í—ã–≤–µ—Å—Ç–∏ —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ã
echo "‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ"
echo "üìä –°—Ç–∞—Ç–∏—Å—Ç–∏–∫–∞ –ø—Ä–æ–≤–µ—Ä–∫–∏ —Å—Å—ã–ª–æ–∫"
echo "‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ"
echo ""
echo "üìÑ –ü—Ä–æ–≤–µ—Ä–µ–Ω–æ —Ñ–∞–π–ª–æ–≤:      $FILE_COUNT"
echo "üîó –í—Å–µ–≥–æ –≤–Ω—É—Ç—Ä–µ–Ω–Ω–∏—Ö —Å—Å—ã–ª–æ–∫: $TOTAL_LINKS"
echo "üåê –í–Ω–µ—à–Ω–∏—Ö —Å—Å—ã–ª–æ–∫:        $EXTERNAL_LINKS"
echo "‚ùå –ë–∏—Ç—ã—Ö —Å—Å—ã–ª–æ–∫:          $BROKEN_LINKS"
echo ""

if [ $BROKEN_LINKS -gt 0 ]; then
    echo "‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ"
    echo "‚ùå –ù–∞–π–¥–µ–Ω—ã –±–∏—Ç—ã–µ —Å—Å—ã–ª–∫–∏:"
    echo "‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ"
    echo ""
    cat "$TEMP_FILE"
    echo "‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ"
    echo ""
    echo "‚ùå FAILED: –û–±–Ω–∞—Ä—É–∂–µ–Ω–æ $BROKEN_LINKS –±–∏—Ç—ã—Ö —Å—Å—ã–ª–æ–∫"
    echo ""
    echo "üí° –†–µ–∫–æ–º–µ–Ω–¥–∞—Ü–∏–∏:"
    echo "   1. –ü—Ä–æ–≤–µ—Ä—å—Ç–µ –ø—É—Ç–∏ –∫ —Ñ–∞–π–ª–∞–º –≤ —É–∫–∞–∑–∞–Ω–Ω—ã—Ö –¥–æ–∫—É–º–µ–Ω—Ç–∞—Ö"
    echo "   2. –£–±–µ–¥–∏—Ç–µ—Å—å, —á—Ç–æ —Ü–µ–ª–µ–≤—ã–µ —Ñ–∞–π–ª—ã —Å—É—â–µ—Å—Ç–≤—É—é—Ç"
    echo "   3. –û–±–Ω–æ–≤–∏—Ç–µ —Å—Å—ã–ª–∫–∏ –Ω–∞ –ø—Ä–∞–≤–∏–ª—å–Ω—ã–µ –ø—É—Ç–∏"
    echo ""
    rm -f "$TEMP_FILE"
    exit 1
else
    echo "‚úÖ PASSED: –í—Å–µ –≤–Ω—É—Ç—Ä–µ–Ω–Ω–∏–µ —Å—Å—ã–ª–∫–∏ –∫–æ—Ä—Ä–µ–∫—Ç–Ω—ã!"
    echo ""
    echo "üìà –ö–∞—á–µ—Å—Ç–≤–æ –¥–æ–∫—É–º–µ–Ω—Ç–∞—Ü–∏–∏: –û–¢–õ–ò–ß–ù–û"
    echo ""
    rm -f "$TEMP_FILE"
    exit 0
fi

